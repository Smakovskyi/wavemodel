		public static float graphStep = 0.01f;
		public static float graphTime = 0.01f;

		// int Step = 100;
		public static int Nx = 30; // 300; // 15000 / Step;
		public static int Ny = 30; // 300; // 15000 / Step;
		public static int Nz = 26; // 30;  // 1500  / Step;

