		// speed profile from 0m to -2500m with a step 100m
		public static float[] BathC = {
				1500,							//    0 m
				1505, 1515, 1530, 1525, 1520,	//  500 m
				1515, 1510, 1505, 1500, 1495,	// 1000 m
				1490, 1485, 1480, 1470, 1460,	// 1500 m
